# The print() function takes an argument between its parentheses and displays the value in the terminal.

print("Hello World")

# In the code above, the print() function is given the argument "Hello World" and you'll notice that it 
# displays only Hello World in the terminal without the quotation marks (""). That's because "Hello World"
# is what we call a string, which is text surrounded by quotation marks ("") or apostrophe marks (''). Strings
# are peices of text-based data that we can use in our program. The reason they need to be enclosed by 
# quotation marks ("") or apostrophe marks ('') is because it distinguishes them as data as opposed to normal 
# code like the print() function itself for example.